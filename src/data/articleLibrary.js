export function getCategoryBySlug(slug) {
  return ARTICLE_CATEGORIES.find((category) => category.slug === slug);
}

export function getArticlesByCategorySlug(categorySlug) {
  return ARTICLES.filter((article) => article.categorySlug === categorySlug);
}

export function getArticleBySlugs(categorySlug, articleSlug) {
  return ARTICLES.find(
    (article) => article.categorySlug === categorySlug && article.slug === articleSlug
  );
}
